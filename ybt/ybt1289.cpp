#define mset(mem, val) memset(mem, val, sizeof(mem))

int dp[1010], tot;
int main()
{
#ifdef __DEBUG__
    freopen("in.txt", "r", stdin);
    freopen("out.txt", "w", stdout);
#endif
    int n;
    readi(n);
    for (int i = 0; i < n; i++)
    {
        int t;
        readi(t);
        int p = tot;
        while (p > 0 && dp[p - 1] < t)
            p--;
        dp[p] = t;
        tot = max(tot, p + 1);
    }
    printf("%d\n", tot);
    return 0;
}