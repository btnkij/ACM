bool adj[300][300];
void floyd(int n)
{
    for(int k = 1; k <= n; k++)
    {
        for(int i = 1; i <= n; i++)
        {
            for(int j = 1; j <= n; j++)
            {
                adj[i][j] = adj[i][j] || adj[i][k] && adj[k][j];
            }
        }
    }
}
