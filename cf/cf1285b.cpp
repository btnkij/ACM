ll maxseq(ll* begin,ll* end)
{
    ll dp=*begin,ans=*begin;
    for(ll* p=begin+1;p!=end;p++)
    {
        dp=max(dp+*p,*p);
        ans=max(ans,dp);
    }
    return ans;
}
